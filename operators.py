#Arithmetic operators
x = 20
y = 3

print('x + y =',x+y)

print('x - y =',x-y)

print('x * y =',x*y)

print('x / y =',x/y)

print('x // y =',x//y)    #only int part

print('x ** y =',x**y)     #power


#comparision operator
x = 89
y = 190
print('x > y is',x>y)

print('x < y is',x<y)

print('x == y is',x==y)

print('x != y is',x!=y)

print('x >= y is',x>=y)

print('x <= y is',x<=y)

#Logical Operators 
x = True
y = False

print('x and y is',x and y)

print('x or y is',x or y)

print('not x is',not x)

#bitwise operator
#Bitwise AND	
print(x & y) 

#Bitwise OR	

print(x | y)

#Bitwise NOT	
print(~x) 

#Bitwise XOR	
print(x ^ y)

#Bitwise right shift	
print(x >> 2)

#left
print(x << 2)

#Membership operators in Python
x = 'Hello world'
y = {1:'a',2:'b'}

print('H' in x)

print('hello' not in x)

print(1 in y)

print('a' in y)
